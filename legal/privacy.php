2.4 Αποδέκτες και διαβιβάσεις
Η Nerally ενδέχεται να διαβιβάζει δεδομένα προσωπικού χαρακτήρα σε άλλες θυγατρικές εταιρείες ή σε τρίτους, αλλά μόνο εάν και στο βαθμό που αυτή η διαβίβαση απαιτείται αυστηρά για τους αναφερθέντες ως άνω σκοπούς.
Η Nerally ενδέχεται να διαβιβάζει δεδομένα προσωπικού χαρακτήρα σε δικαστικές, διοικητικές, φορολογικές, τελωνειακές, διαιτητικές αρχές ή άλλες δημόσιες αρχές ρυθμιστικούς φορείς και δικηγόρους εάν αυτό είναι απαραίτητο για τη συμμόρφωση με τη νομοθεσία ή και για τη θεμελίωση, άσκηση ή υπεράσπιση νομικών αξιώσεών της.
Περαιτέρω, η Nerally ενδέχεται να αναθέτει μέρος ή όλη την ως άνω επεξεργασία σε τρίτα μέρη (εκτελούντες την επεξεργασία) συμπεριλαμβανομένων των διευθυντών και υπαλλήλων αυτών:
σε μέρη τα οποία έχουν συμβληθεί με την Nerally για την προώθηση των υπηρεσιών της Nerally, για την παροχή ταχυδρομικών υπηρεσιών, υπηρεσιών μηχανογραφικής υποστήριξης, υπηρεσιών τήρησης αρχείου, υπηρεσιών διεξαγωγής ερευνών, υπηρεσιών πληροφορικής, διαφημιστικών υπηρεσιών, τραπεζικά και πιστωτικά ιδρύματα, εταιρίες ορκωτών λογιστών.
Σε μέρη τα οποία έχουν συμβληθεί με την Nerally για την αξιολόγηση της πιστοληπτικής ικανότητας του πελάτη να εκπληρώσει τις υποχρεώσεις του από τη σύμβαση, σε εταιρείες ενημέρωσης οφειλετών, με σκοπό τη σχετική ενημέρωση του πελάτη βάσει του ν. 3758/2009, όπως τροποποιήθηκε και ισχύει (οι διευθυντές και οι υπάλληλοι αυτών), σε δικηγόρους και δικηγορικές εταιρείες.
Σε αυτές τις περιπτώσεις, διασφαλίζουμε μέσω συμβατικών όρων και τακτικών ελέγχων ότι, εάν και εφόσον έχουν πρόσβαση σε προσωπικά δεδομένα, τηρείται επαρκώς η νομοθεσία για την προστασία τους.
Οι αποδέκτες των δεδομένων προσωπικού χαρακτήρα ενδέχεται να είναι εγκατεστημένοι εκτός του Ευρωπαϊκού Οικονομικού Χώρου. Σε αυτές τις περιπτώσεις, η Nerally λαμβάνει μέτρα ώστε να εφαρμόζει επαρκείς και κατάλληλες εγγυήσεις για την προστασία των δεδομένων προσωπικού χαρακτήρα με άλλα μέσα κυρίως με τη χρήση των τυποποιημένων δεσμευτικών ρητρών από την ΕΕ.
3. Χρόνος Τήρησης Δεδομένων
Η Nerally θα τηρεί τα προσωπικά σας δεδομένα για όσο διάστημα απαιτείται για την εκπλήρωση των σκοπών που περιγράφονται στην παρούσα πολιτική, εκτός εάν η ισχύουσα νομοθεσία επιτάσσει ή επιτρέπει μεγαλύτερο χρονικό διάστημα. Τα κριτήρια που διέπουν τον καθορισμό του χρόνου τήρησης των δεδομένων περιλαμβάνουν τα εξής: (α) όσο διαρκεί η μεταξύ μας συμβατική σχέση, (β) όσο απαιτείται προκειμένου να βρίσκεται η Nerally σε συμμόρφωση με νόμιμη υποχρέωση που την βαρύνει, (γ) όσο απαιτείται ενόψει της νομικής θέσης που βρίσκεται η Nerally (όπως υπεράσπιση δικαιωμάτων ενώπιον δικαστηρίων, έλεγχοι ρυθμιστικών αρχών κ.λπ.).
4. Τεχνικά και οργανωτικά μέτρα
Η Nerally εφαρμόζει αποτελεσματικά, τόσο κατά τη στιγμή του καθορισμού των μέσων επεξεργασίας όσο και κατά τη στιγμή της επεξεργασίας, κατάλληλα τεχνικά και οργανωτικά μέτρα, όπως η ψευδωνυμοποίηση, σχεδιασμένα για την εφαρμογή αρχών προστασίας των δεδομένων, όπως η ελαχιστοποίηση των δεδομένων, και την ενσωμάτωση των απαραίτητων εγγυήσεων στην επεξεργασία αυτή κατά τρόπο ώστε να πληρούνται οι απαιτήσεις της ισχύουσας νομοθεσίας και να προστατεύονται τα δικαιώματα των φυσικών προσώπων.
5. Δικαίωμα ανάκλησης της συγκατάθεσης
Σε περίπτωση που δηλώσατε τη συγκατάθεσή σας για την επεξεργασία συγκεκριμένων δεδομένων προσωπικού χαρακτήρα από την Nerally, έχετε το δικαίωμα να ανακαλέσετε τη συγκατάθεση ανά πάσα στιγμή, με μελλοντική ισχύ. Η ανάκληση της συγκατάθεσης δεν επηρεάζει τη νομιμότητα της επεξεργασίας που βασίστηκε στη συγκατάθεση προτού αυτή ανακληθεί. Σε περίπτωση ανάκλησης της συγκατάθεσης, η Nerally δύναται να επεξεργάζεται περαιτέρω τα δεδομένα προσωπικού χαρακτήρα, μόνο σε περιπτώσεις που υφίσταται άλλος νομικός λόγος για την επεξεργασία.
6. Δικαιώματα υποκειμένου
Υπό την ισχύουσα νομοθεσία περί προστασίας των δεδομένων προσωπικού χαρακτήρα και εφόσον πληρούνται οι σχετικές νόμιμες προϋποθέσεις έχετε τα ακόλουθα δικαιώματα:
6.1  Δικαίωμα πρόσβασης
Έχετε δικαίωμα να ενημερωθείτε εάν η Nerally επεξεργάζεται δεδομένα σας, να έχετε πρόσβαση στα δεδομένα και να λάβετε συμπληρωματικές πληροφορίες σχετικά με την επεξεργασία τους.
6.2 Δικαίωμα διόρθωσης
Έχετε δικαίωμα να αιτηθείτε την επικαιροποίηση, διόρθωση, συμπλήρωση των  προσωπικών σας δεδομένων.
6.3 Δικαίωμα διαγραφής
Έχετε δικαίωμα να υποβάλετε αίτημα διαγραφής των προσωπικών σας δεδομένων, το οποίο θα ικανοποιείται υπό την προϋπόθεση ότι δε συντρέχει άλλη νομική βάση επεξεργασίας (όπως ενδεικτικά υποχρέωση επεξεργασίας προσωπικών δεδομένων που επιβάλλεται από το νόμο).
6.4 Δικαίωμα περιορισμού της επεξεργασίας
Έχετε δικαίωμα να ζητήσετε περιορισμό της επεξεργασίας των προσωπικών σας δεδομένων στις ακόλουθες περιπτώσεις: (α) όταν αμφισβητείτε την ακρίβεια των προσωπικών δεδομένων και μέχρι να γίνει επαλήθευση, (β) όταν αντιτίθεστε στη διαγραφή προσωπικών δεδομένων και ζητάτε αντί διαγραφής τον περιορισμό χρήσης αυτών, (γ) όταν τα προσωπικά δεδομένα δεν χρειάζονται για τους σκοπούς επεξεργασίας, σας είναι ωστόσο απαραίτητα για τη θεμελίωση, άσκηση, υποστήριξη νομικών αξιώσεων, και (δ) όταν εναντιώνεστε στην επεξεργασία και μέχρι να γίνει επαλήθευση ότι υπάρχουν νόμιμοι λόγοι που μας αφορούν και υπερισχύουν των λόγων για τους οποίους εναντιώνεστε στην επεξεργασία.
6.5 Δικαίωμα εναντίωσης στην επεξεργασία
Έχετε δικαίωμα να εναντιωθείτε ανά πάσα στιγμή στην επεξεργασία των προσωπικών σας δεδομένων όταν αυτή βασίζεται στην νομική βάση (Άρθρο 6 (1) (ε) ή (στ) του Γενικού Κανονισμού) η οποία θα ικανοποιηθεί εκτός εάν η Nerally καταδείξει επιτακτικούς και νόμιμους λόγους για την επεξεργασία τους.
6.6 Δικαίωμα στη φορητότητα
Έχετε δικαίωμα να λάβετε χωρίς χρέωση τα προσωπικά σας δεδομένα σε δομημένο, κοινώς χρησιμοποιούμενο και αναγνώσιμο από μηχανήματα μορφότυπο ή να αιτηθείτε, εφόσον είναι τεχνικά εφικτό, να διαβιβάσουμε τα δεδομένα απευθείας σε άλλον υπεύθυνο επεξεργασίας.
6.7 Δικαίωμα εναντίωσης σε λήψη απόφασης βάσει αυτοματοποιημένης επεξεργασίας
Έχετε το δικαίωμα να αιτηθείτε στην εξαίρεσή σας από τη λήψη αποφάσεων που βασίζεται σε αυτοματοποιημένη επεξεργασία, συμπεριλαμβανομένης της κατάρτισης προφίλ.
